  function sorting(){
   var price= document.querySelector("#filter").value;
   if(price=="lowest")
   {
      var com =  data.sort(function(a,b){
            return a.MRP-b.MRP
        })
    }
    if(price=="highest")
    {
      var com=data.sort(function(a,b){
        return b.MRP-a.MRP
      })
    }


    if(price=="asc")
    {
      var com=data.sort(function(a,b){
       return a.type>b.type?1:b.type>a.type?-1:0
      })
    }

    if(price=="des")
    {
      var com=data.sort(function(a,b){
        return a.type>b.type?-1:b.type>a.type?1:0
      })
    }
    showProducts(com)
  }
  


   
